    <h1 id="wwIntro"><span>Blended Learning 2.0:</span> A Social Constructivist Approach</h1>

    <p>
        Many people believe that "disruptive" technology has the power to enhance 
        innovation in higher education.  It is our belief that innovative technology 
        will offer non-invasive solutions that energize and expand current pedagogy.  
        Rather than disrupt education or lecture, we believe that Web 2.0 technology 
        will seamlessly evolve classroom teaching into blended environments for 
        deeper learning experiences. 
    </p>

    <p>
        In today's individualistic and competitive learning environments, we are 
        throwing away tremendous amounts of pedagogical value by failing to 
        provide social tools that leverage differences in understanding between 
        the students themselves.  Technology should naturally supplement and 
        enhance classroom teaching by providing a social constructive environment 
        for peer interaction and learning outside the classroom.  Social media, 
        if designed correctly, facilitates many-to-many collaboration for the 
        entire class, maximizing learning potential and alleviating teaching 
        pressure simultaneously.  Multimodal activity streams provide multiple 
        representations of course material from multiple perspectives, which help 
        deepen learning in an inclusive manner.
    </p>

    <p>
        There is an enormous body of work in education on constructivist theory 
        in the frameworks of epistemology, learning theory, and pedagogy.  
        The translation of constructivism into pedagogy, however, tends to be 
        fragmented and uncritical, causing an unhelpful and inaccurate 
        dichotomization of "student-centered" and "teacher-centered" pedagogy.  
        Furthermore, translations of constructivism into digital pedagogy have 
        excluded the important process of meaning making through social interaction 
        between the students themselves.  Before designing technology that finally 
        unleashes the power of e-learning in traditional classrooms, it's helpful 
        to identify the basic elements of constructivist and traditional learning 
        theory that are important to deeper learning.  Understanding these 
        elements in the context of practical teaching will shed light on how 
        to design constructivist technology that expands pedagogy spontaneously.
    </p>
    
    <h4 id="wwEpist">Epistemology Is Not Important to the Practice of Teaching</h4>

    <p>
       Epistemology is the branch of philosophy concerned with the nature of 
       knowledge and the extent to which it can be acquired.  Philosophical 
       assumptions about theories of knowledge are complex and controversial.  
       Contrary to traditional thinking in instructional design, epistemology 
       is unrelated to the practice of teaching. 
    </p>
    
    <p>
        Discussions of approaches to teaching as applications of epistemological 
        beliefs have been popular in educational literature for the past few decades.  
        Many designers believe it's important for teachers to execute strategies 
        consistent with their own epistemological beliefs about the subject matter 
        that they teach.  Epistemology, however, is not pedagogy, and attempts to 
        translate it into practice will always be problematic and less than 
        satisfactory.  Assumptions about the nature of knowledge or the extent by 
        which reality can be known have nothing to do with the learning 
        process itself.  Learning within any given discipline requires mastery 
        of the language-based meanings that define disciplinary knowledge and 
        practice.  Constructivist inquiry, facilitated by the use of language, 
        builds all knowledge (including theories of knowledge).  
        <a href="javascript:PopUpShow('figure1')">For a review of significant epistemologies from a 
            constructivist perspective see Figure 1.</a>
    </p>
    
    <p>
        In instructional design, it's popular to label approaches to teaching as 
        either objectivist or constructivist simply because they are polar 
        opposites on an "epistemological continuum".  Objectivists concentrate 
        on direct instruction and constructivists focus on learning, although 
        many designers believe in blended approaches.  Traditionally designers 
        label the lecture or direct instruction as an "objectivist" approach 
        to teaching.  The use of lecture, however, has never been attributed to 
        conscious employment of objectivist approaches, nor do objectivist .
        convictions lead to a traditional way of teaching.  Logically it's 
        reasonable to imagine that the lecture is more inline with empiricist 
        or constructivist epistemologies because teaching strategies are meant 
        to facilitate knowledge construction.  Lectures provide the scaffolding 
        or foundation for more constructive learning outside of class.  
        The use of lecture is particularly effective when it communicates 
        information students cannot learn independently, a key concept in Vygotsky's 
        constructivist learning theory.  Efforts to label the lecture as an 
        objectivist approach are misleading and uncritical.
    </p>
    
    <h4 id="wwBrief">A Brief Overview of the Evolution of Pedagogy Through the Turn of the 20<sup>th</sup> Century</h4>

    <p>
        Before translating constructivist learning theory into practice, it's 
        helpful to understand the evolution of pedagogy that grew out of the 
        monasteries of the Roman Catholic Church (<a href="javascript:PopUpShow('figure2')">Figure 2</a>).  
        The instructional 
        methods of medieval universities did not faintly resemble those used today.  
        Debates pitting instructor against instructor or student against student 
        formed the majority of instruction.  The 17th century scientist and 
        father of modern education, John Amos Comenius, promulgated a reformed 
        system of universal education that was widely used in Europe.  
        His influence on educational thought is comparable with his contemporaries, 
        Bacon and Descartes, on science and philosophy.  Largely inspired by them, 
        Comenius was the first to apply in a systematic manner the principles of 
        investigation and thought to education.  His general theory of education 
        put forth the idea of "education according to nature".  He introduced 
        pictorial textbooks written in native language instead of Latin, 
        supported the development of logical thinking by moving away from rote 
        learning, and opened doors to education for women and impoverished children.
    </p>
    
    <p>
        After Comenius, there are a number of reformers throughout the modern era 
        that helped evolve more constructive methods of teaching in response to 
        reoccurring tendencies to slip into mechanical formalism.  John Locke put 
        forth the idea that the mind of a child is a <span class="wg-font-bi">tabula rasa</span> or "blank slate" 
        that is "filled" by experience in efforts to encourage better teaching 
        and equal opportunity.  In <span class="wg-font-bi">Some Thoughts Concerning Education</span>, he writes 
        that teachers "should remember that his business is not so much to 
        teach [the child] all that is knowable, as to raise in him a love and 
        esteem of knowledge; and to put him in the right way of knowing and 
        improving himself".  He also fought against the long hours wasted 
        learning Latin.  Other philosophers eagerly adopted Locke's idea that 
        people's minds were shaped through their experiences and thus through 
        their education.  Locke played an important role in defining 18th century 
        educational theory in England and the U.S. 
    </p>
    
    <p>
        Towards the end of the 18th century, the growth of education attracted 
        interest from European governments, leading to the first attempts to 
        create what might be considered academic rationales for teaching methods.  
        In Germany, Johann Herbart, in Platonic tradition, emphasized the 
        connection between individual development and societal contribution.  
        He subscribed to Locke's empiricist viewpoint involving the <span class="wg-font-bi">tabula 
        rasa</span> and was an advocate of guided learning by doing.  Herbart was the 
        first to apply the idea of apperception, or the process by which new 
        experience is perceived in relation to past experience, to education.  
        His pedagogy spread to the U.K., France, and the U.S.
    </p>

    <p>
        By the turn of the 20<sup>th</sup> century, the educational landscape degenerated 
        to mechanical formalism, which led to the progressive education 
        movements in Europe and the U.S.  There are a number of important 
        reformers during this period.  Maria Montessori and John Dewey, 
        in particular, encouraged practices of teaching that blended direct 
        instruction with constructive activities, including social interaction 
        between the students themselves.  The Montessori method, which spread 
        throughout the world, stressed the importance of social interaction and 
        an interactive curriculum.  John Dewey, a noted philosopher, psychologist, 
        and father of progressive education, presented his educational theories in 
        <span class="wg-font-bi">My Pedagogic Creed</span> (1897), 
        <span class="wg-font-bi">The School and Society</span> (1900), 
        <span class="wg-font-bi">The Child and the Curriculum</span> (1902), 
        <span class="wg-font-bi">Democracy and Education</span> (1916), and 
        <span class="wg-font-bi">Experience and Education</span> (1938).  Dewey believed learning is a social phenomenon as 
        well as an individual experience.  He was alarmed, however, by many of 
        the "child-centered" excesses of teachers who claimed to be his followers.  
        Dewey maintained that it is "adult knowledge" that guides students 
        throughout the learning process.
    </p>
    
    <p>
        The evolution of pedagogy includes a number of constructive contributions 
        to the practice of teaching that were developed before Jean Piaget's 
        constructivist learning theory in the 60's.  Today, constructivist 
        teaching is based on the belief that learning occurs when students 
        are actively involved in a process of meaning and knowledge construction.  
        The idea that students must be active in the learning process is 
        often understood to imply a diminishing role for the teacher in the 
        classroom, which has dichotomized methods of teaching into "student-centered" 
        versus "teacher-centered" or constructivist versus objectivist.  
        The translation of constructivism into practice in the context of power 
        and authority has been misleading.
    </p>

    <h4 id="wwKey">Two Key Elements From the Three Major Variants of Constructivism Are Critical to Deeper Learning</h4>

    <p>
        There are three major variants of constructivist learning theory:  
        social constructivism, cognitive constructivism, and radical 
        constructivism (<a href="javascript:PopUpShow('figure3')">for review, see Figure 3</a>).  Although cognitive and 
        radical constructivists defend opposing views on the nature of reality, 
        they are identical from a pedagogical perspective-both support an 
        <span class="wg-font-bi">individual constructive</span> element that is important to an individual's 
        incorporation of new information and experiences into previous understandings.  
        Social constructivists also support an <span class="wg-font-bi">individual constructive</span> element 
        because they share the perspective that learners need to accommodate and 
        assimilate new knowledge.  Teachers have long understood that a more 
        thorough understanding of lecture material requires the students to study 
        outside of class.  Assignments and exercises are designed by the teacher to 
        stimulate cognitive constructive understandings of subject material.
    </p>

    <p>
        Vygotsky's constructivism adds a <span class="wg-font-bi">social constructive</span> element that stresses 
        the importance of collaborative learning, discussions, and reciprocal 
        teaching in the learning process (<a href="javascript:PopUpShow('figure3')">Figure 3</a>).  
        Social constructivists consider 
        language as the primary conduit for learning, and discourse as the most 
        powerful method for refining understanding into increasingly higher-order forms.  
        Social constructivist approaches to teaching recognize an important role for 
        peer learning and social interaction mediated by dialogue in addition to 
        individual learning.  Although many teachers believe in social approaches, 
        there are currently no technologies that support social constructive learning 
        in an inclusive manner.
    </p>
    
    <h4 id="wwTraditional">Traditional Learning Theories Feature One Key Element Important to Teaching Practice</h4>

    <p>
        Decades of research on humans and other species attest to the significance 
        of social learning through observation for the acquisition of adaptive 
        information and skills.  The social learning theory proposed 
        by Albert Bandura has become perhaps the most influential theory of 
        learning in education (<a href="javascript:PopUpShow('figure4')">see Figure 4</a>).  While rooted in many of the 
        basic concepts of behaviorism, Bandura understood that learning could 
        occur without a change in behavior.  He convincingly explains that 
        people can learn though observation in social contexts such as direct 
        instruction, modeling, and imitation.
    </p>

    <p>
        From a pedagogical perspective, his theory features a <span class="wg-font-bi">social observational</span>
        element important to the effective delivery of lecture.  Well-crafted 
        lectures can facilitate cognition at a "surface level" and provide the 
        scaffolding or foundation for more constructive learning outside of class.  
        Dewey's educational philosophy and Bandura's learning theory reinforce the 
        idea that social and cognitive elements are inseparably related in 
        teaching and learning.  Traditional conceptions of social learning theory, 
        however, do not consider the learning that takes place between the students 
        themselves. 
    </p>
    
    <h4 id="wwTechnology">Social Constructive Technology Bridges Learning Theory and Teaching Practice</h4>

    <p>
        The translation of learning theory into practice is more accurate if the 
        basic elements of traditional and constructivist theories are identified 
        in top-down fashion rather than in the context of authority or power.  
        This analysis finds three elements of learning theory that are critical 
        to deeper teaching and learning:  
        <span class="wg-font-bi">social observational</span>, 
        <span class="wg-font-bi">individual constructive</span>, and 
        <span class="wg-font-bi">social constructive</span> (<a href="javascript:PopUpShow('figure5')">see Figure 5</a>). 
    </p>

    <p>
        In traditional learning environments, students have been responsible 
        for their own constructivist learning outside of class.  In flipped 
        classrooms, with an exclusive focus on constructive activities, 
        the scaffolding supplied by well-crafted lectures is lost.  
        Constructivist teaching is an integrative approach that does not 
        exclude lecture or direct instruction.  In today's educational landscape, 
        it's necessary to supplement classroom teaching with digital environments 
        that facilitate social constructive teaching and learning.  
        The use of social tools that leverage the different levels of 
        understanding between peers is rare in education.  Blended environments 
        using social constructive tools reflect all three elements important to 
        deeper learning.
    </p>
    
    <h4 id="wwBeyond">Beyond Student-Centered and Teacher-Centered Pedagogy</h4>

    <p>
        Blended teaching approaches reject the dichotomization of modes of effective 
        teaching as either student- or teacher-centered.  Because teachers 
        command greater expertise about the subject matter, they are in the 
        best position to select content, provide instruction, and guide learning 
        activities.  Robust educational experiences guide student participation 
        in a full range of disciplinary activities:  lectures and tests, 
        collaborative learning, constructivist inquiry/Q&A, and independent reading.  
        The sociological elements in traditional and constructivist learning 
        theories are important to deeper learning inside and outside of the 
        classroom (<a href="javascript:PopUpShow('figure6')">see Figure 6</a>). 
    </p>
    
    <h4 id="wwSocial">Social Constructive Technology Is Missing in Education</h4>

    <p>
        Various educational technologies support any one of the three elements 
        of learning theory (<a href="javascript:PopUpShow('figure7')">see Figure 7</a>).  Synchronous technologies support 
        either <span class="wg-font-i">social observational</span> or <span class="wg-font-i">social constructive</span> elements.  
        Asynchronous technologies support either <span class="wg-font-i">individual constructive</span> or 
        <span class="wg-font-i">social constructive</span> elements for deeper learning.  The LMS discussion 
        forum is virtually dead in education.  Social, asynchronous technology 
        that leverages the pedagogical value of the students themselves is 
        missing in education. 
    </p>
    
    <h4 id="wwActivity">Activity Streams Organically Facilitate Peer-Mediated Multimodal Learning</h4>

    <p>
       Socrates and the ancient Greek philosophers were the first to understand 
       the central role dialogue plays in the learning process.  The Socratic 
       dialogue, or circle, is a social learning method in which groups of people 
       are guided by a facilitator.  Taking a design cue from Socrates, 
       web-learning environments need to only support dialogue and interaction 
       in many-to-many fashion.  Activity streams powered by social media 
       technology can mediate exchange of multiple combinations of digital 
       content-text-based, documents, equation editor, web links, images, 
       and videos-that serve to both streamline and deepen communication.  
       Multimodal activity streams provide multiple representations of course 
       material from multiple perspectives to help deepen learning in an inclusive 
       manner.  Many-to-many environments also provide a virtual sense of 
       belonging and community that helps prevent feelings of isolation during 
       the constructive learning process. 
    </p>
    
    <h4 id="wwConclusions">Conclusions</h4>

    <p>
        Dialogue underlies all knowledge-creating disciplines and professions. 
        Dialogic and visual literacies in the 21st Century are indispensible to 
        life in the information age. Blended environments using technology 
        specifically designed to support social constructivist learning have 
        compelling potential to deepen learning. Adding more student voices, 
        perspectives, and content is virtually guaranteed to energize 
        teaching and learning.
    </p>
    

                        assimilation, and language-based interactions. The world is 
                        independent of human minds, like in objectivism, but knowledge 
                        of the world is always a human and social construction.
                    </p>
                </div>
                <div class="clear"></div>
            </div>
            <div class="figure-footer">
                <p><span>Figure 1. Epistemology is not important in teaching and learning.</span>
                Constructivist inquiry, facilitated by the use of language, builds 
                all knowledge—rational, empirical, objective, or constructive—and 
                is emerging as a powerful model for explaining how students acquire 
                deeper knowledge. Rational knowledge evolved from mathematical realism. 
                Empirical knowledge began with Aristotle, which lead to a 
                longstanding debate between rationalism and empiricism in the 
                modern era. Transcendental idealism and pragmatism were meant 
                to bridge the gap between the two extremes. Today, objectivist 
                or constructivist assumptions on the natures of knowledge and 
                reality are not important in pedagogy. Deeper learning requires 
                mastery of the language-based meanings that define disciplinary 
                knowledge or practice.</p>
            </div>
        </div>
    </div>

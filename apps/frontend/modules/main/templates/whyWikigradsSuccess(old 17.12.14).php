    <h1 id="introduction">Why <span>WikiGrads</span>?</h1>

    <h4 id="why">Why bother supplementing your lecture?</h4>

    <p>The United States ranks last among industrialized nations in college completion rates <a href="#ref-1">[1]</a>.
        Despite advances in research and theory in understanding student success, student attrition
        continues to be pervasive <a href="#ref-2">[2, 3]</a>.  Competitive class climates, over-packed curricula, and
        the overt “weed-out” attitude of some college faculty are cited most often as reasons for
        abandoning programs in STEM or dropping out of school entirely.
    </p>

    <p>In the face of growing economic, environmental, and social challenges, several foundations
        recently charged the National Research Council (NRC) to recommend changes in instructional
        strategy, and to define the set of key skills that enable “deeper learning”, “21st century skills”,
        and “student-centered learning”, among other competency-based labels <a href="#ref-4">[4]</a>.  In their 2012 report,
        the NRC identified 3 domains of competence for successful transfer of knowledge and skills
        in the 21st century:
    </p>
        <ul>
            <li>Many-to-many communication for the entire classCognitive domain—thinking, reasoning, memory, and related skills</li>
            <li>Cognitive domain—thinking, reasoning, memory, and related skills</li>
            <li>Intrapersonal domain—work ethic and conscientiousness.</li>
        </ul>

    <p>In defining inter- and intrapersonal domains (non-cognitive domains),
        the committee investigated socio-cultural or situated learning theory
        after acknowledging that learning is a process grounded in social relationships
        within a community.  Although not extensively elaborated on by the NRC, there
        is now strong empirical evidence that non-cognitive competencies are more
        significant to first-year student retention than cognitive competency <a href="#ref-5">[5, 6]</a>.
    </p>

    <p>Dr. Alexander Astin, Allan M. Cartter Professor Emeritus of Higher Education
        at UCLA and the most frequently cited author on student success, explains that
        the majority of students who drop out of college share similar cognitive abilities
        as those who succeed but are unable to integrate socially and/or maintain engagement <a href="#ref-7">[7]</a>.
        In that book, Dr. Astin famously writes, “The student’s peer group is the single most powerful
        source of influence on the undergraduate student’s academic and personal development.”
    </p>

    <p>At WikiGrads we believe highly interactive and interconnected communication that supports
        both student-student and student-instructor interaction is central to “student-centered learning”,
        and provides the necessary infrastructure to enhance the non-cognitive domains of competency.
    </p>


    <h4 id="importance">The NRC and AAHE highlight the importance of peer interaction and active learning</h4>

    <p>After extensive review of the literature, the NRC concludes that active forms of learning,
        which encourage elaboration and questioning, are critical to deeper, competency-based learning.
        Their findings largely echo the famous and widely distributed “Seven Principles for Good Practice
        in Undergraduate Education,” published in 1987 by the American Association of Higher Education (see table) <a href="#ref-8">[8]</a>.
    </p>

    <table>
        <tr>
            <th>NRC (2012)</th>
            <th>AAHE Bulletin (1987; showing 1 – 4 out of 7)</th>
        </tr>
        <tr>
            <td>Encourage elaboration, questioning, and explanation </td>
            <td>Good practice encourages contacts between students and faculty</td>
        </tr>
        <tr>
            <td>Diversify instructional strategies and representations of concepts and tasks</td>
            <td>Good practice develops reciprocity and cooperation among students</td>
        </tr>
        <tr>
            <td>Engage learners in challenging tasks; prime student motivation</td>
            <td>Good practice uses active learning techniques</td>
        </tr>
        <tr>
            <td>Use formative assessments</td>
            <td>Good practice gives prompt feedback</td>
        </tr>
    </table>

    <p>The AAHE guidelines, which are still considered highly relevant today,
        are a meta-analysis of 50 years of research on good teaching principles
        by Arthur Chickering and Zelda Gamson.  The recent NRC report and the 1987
        AAHE Bulletin both highlight the importance of student-centered instructional
        strategies that encourage discussion and peer-peer interaction.
    </p>

    <p>Constructive pedagogical approaches that supplement lecture typically include
        collaborative or social learning activities.  Collaborative learning environments,
        although not extensively discussed by the NRC, are the best-studied pedagogical strategies
        in the history of education research.  There are over 1,200 research studies on the topic
        dating as far back as 1898 <a href="#ref-9">[9]</a>.  Meta-analyses reveal significant positive and measurable
        outcomes on students at various cognitive levels and in various disciplines; the inclusion
        of collaborative learning strategies within traditional and hybrid courses significantly improved
        transfer of knowledge compared to traditional courses without use of active learning techniques <a href="#ref-10">[10]</a>.
    </p>


    <h4 id="collaboration">Collaboration, the forgotten pedagogy</h4>

    <p>Research studies overwhelmingly favor collaborative learning as the most effective form of knowledge transfer.
        Today, many instructors are aware of the problems associated with the traditional lecture but understandably
        remain hesitant to diversify their teaching strategy for various reasons.
    </p>

    <p>Historically institutions view collaborative learning as cheating because traditional pedagogy
        almost exclusively recognizes individual effort and competition.  Today, collaborative learning
        is relatively unfamiliar to instructors, as well as their students <a href="#ref-10">[10]</a>.  Perhaps the biggest
        impediment to diversification is fear of the loss of content coverage during lecture.  Collaborative,
        group interactions take longer than well-crafted, efficient lectures.  Students need time to accumulate
        enough information and reach consensus.  Furthermore, lectures are not conducive to class discussion;
        many instructors acknowledge that it’s hard to get students to talk, or at least talk “well”.
    </p>


    <h4 id="what-is-wrong">What is wrong with the LMS discussion forum?</h4>

    <p>There are numerous studies highlighting an important role of asynchronous communication
        in facilitating deeper learning outside of lecture.  The discussion forum is a ubiquitous
        component of every proprietary and open source LMS.
    </p>

    <p>However, today, online discussion forums typically fail to generate student-student or
        student-instructor interaction because these platforms are not social and lack the infrastructure
        to support highly interconnected communication and collaboration.  Discussion forums do not dynamically
        organize or compress conversation threads, nor do they support the ability to generate conversations around
        documents, images, or hyperlinks.  Furthermore, the temporal separation of postings causes discussions to appear
        fragmented and discontinuous, sacrificing the clarity and immediacy that can be achieved in social communication feeds.
    </p>


    <h4 id="how-can-socialized">How can socialized communication promote equity in academic achievement?</h4>

    <p>Many historically underserved students—African American, Latino/a, and American
        Indian—continue to experience differential retention rates and inequities in academic achievement,
        in part because of limited integration with peers from the same minority group and/or peers from other groups.
        In addition, a subset of students from all ethnic groups experience significant difficulties integrating with peers <a href="#ref-7">[7]</a>.
        Whole-class interconnectedness supports equity in academic achievement by providing impromptu peer learning and reciprocity to everyone.
    </p>


    <h4 id="conclusion">Conclusion</h4>

    <p>Implementation of a socialized communication platform that facilitates peer-peer and private student-instructor
        communication represents a relatively easy solution to improve the interpersonal domain of all students.
        Academic failure is not a problem that is exclusively related to lack of student accountability, motivation,
        and academic preparation.  More often than not, student success depends on peer-peer interaction and learning.
        When technology is used to extend and socialize communication for everyone, it can make a world of difference.
    </p>



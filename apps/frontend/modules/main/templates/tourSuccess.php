                            <div id="wiki-post-content-5785" class="wg-post-row-2">
                                <div>
                                    <p>In the Battle of Algiers, I thought that the scapegoating that happens throughout was interesting,
                                    and I want to talk about it a bit more. I was struck by the fact that violence seemed to continue
                                    regardless of the fact that so many innocent people were dying. I think that it must be human nature
                                    to seek out someone to blame when horrible things happens, but realistically, is anyone in the
                                    film at fault for the horror? Obviously, we see men pulling triggers and women leaving bombs,
                                    but I personally couldn’t find any one of them to be the antagonist in this revolutionary story.
                                    We could point fingers at the upper echelons of French society that colonized the Algerians to begin with.
                                    or be angry with the Algerians for violently rebelling and taking the lives of innocent bystanders.
                                    But these accusations become cyclical as violence breeds more violence and death. If we blamed human
                                    nature - seeing it as inherently flawed -  what would that mean for our analysis of revolution?
                                    Would the revolutionary process be different if we believed that our problems were inherent rather than curable?
                                    Would we even try to revolt?</p>
                                    <div id ="tour-step8"></div>
                                    <p>In Fanon’s chapter on violence,  Fanon suggests that decolonization is a fight between two forces naturally
                                    distinct and separate. But in his chapter concerning national consciousness he seems to suggest that this
                                    division continues to cause problems after decolonization. My questions is, do you see Fanon as using
                                    education as the distinguishing characteristic between the colonized and colonizers, and do you see
                                    Fanon as suggesting that western capitalism created halfbreeds within the colonized people?</p>

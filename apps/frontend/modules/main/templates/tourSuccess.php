                                    <div id="wiki-post-content-3678" class="wg-post-row-2">
                                        <div>
                                            It’s when the shells are “happy.” So, for example, the group 2 elements are fairly happy because
                                            they have a full s-orbital. Therefore their electron affinity is lower than that of the group 1
                                            and group 13 elements. The group 15 elements are fairly happy too, because they have a half-full
                                            p-orbital, so their electron affinity is slightly less than the group 14 and the group 16 elements.
                                            Obviously, the noble gases are SUPER happy and thus have low electron affinity. I think the pattern
                                            also holds true for the d-block elements. So, group 7 has lower electron affinity
                                            than 6 and 8 (1/2 full d-orbital), and group 12 has lower EA than groups 11 and 13 (full d-orbital).</div>

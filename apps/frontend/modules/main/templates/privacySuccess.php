<h3>BARRIERS TO SOCIAL MEDIA ADOPTION IN HIGHER ED:  PRIVACY & ACADEMIC INTEGRITY</h3>

<p>Although faculty in higher education see considerable potential in the ability of social media to enhance teaching effectiveness and student learning, the majority refrain from using social media sites in a teaching context.  A detailed Pearson survey completed by 8,000 faculty members from all disciplines in higher education on use of social media in teaching identified multiple barriers to widespread adoption (Seaman & Tinti-Kane, 2013).  The primary barriers are:</p>
<ul>
<li>Concerns by faculty about privacy for themselves, their students, and maintaining the class as a private space for free and open discussion. </li>

<li>Concerns about the academic integrity of student submissions.</li>

<li>Fear of creating distraction associated with use of popular social media platforms in an academic setting.</li>

<li>Takes too much time to learn or too difficult to create separate course and personal accounts on public social media platforms (e.g., Facebook or Twitter).</li>
</ul>
<p>According to the Pearson study, only 33% of faculty made use of social media in the classroom at some point in 2013.  Faculty are much more willing to embrace social media in their personal lives than they are in a teaching context.  Use of social media in teaching, however, has increased every year, indicating these concerns are decreasing over time.   It can be expected that the steady increase in classroom use of social media will continue, but its adoption will be very selective unless the barriers are appropriately addressed.</p>


<h3>A SOLUTION</h3>

<p>WikiGrads is a private, social learning network that focuses content exclusively on academic use.  Its independence from giant public social sites eliminates outside distractions.  The platform is simple, intuitive, and takes less than 2 minutes to setup.  Students are encouraged to share and comment on information, and interact with their peers, instructors, and the learning materials themselves.  This brings a new level of engagement and active participation to learning, and allows instructors and students to facilitate community building anytime and anywhere.</p>

<p>Instructors choose if they want to create a public or private social forum for their course.  If a private forum is desired, students are required to enter a code that is only given to the instructor, preventing non-class members from viewing or downloading class information.  If an instructor or student has a concern that an uninvited user has joined the private forum, instructors are easily able to remove them. </p>

<p>In each course-specific forum, all communications (questions, discussions, or course blogs) are continuously organized over time in one convenient location.  In order to maximize student participation/interactivity and academic integrity, communications are equally accessible to all students; private messaging has not been included as a platform feature.</p>

<p>The distribution of course-related documents is an available feature on the platform. Although instructors currently utilize learning management systems to distribute documents, many may find it more convenient to organize both documents and course communications in one location.  To help protect the integrity of student submissions, instructors control how documents are shared in their class:</p>
<ul>
    <li>Open access for any user to upload or download documents.</li>

    <li>Only student-instructor exchange of documents is permitted.  Students are not able to download each other’s documents.</li>

    <li>No document exchange.</li>
</ul>

<p>WikiGrads is a prototype social learning network that has been designed to help overcome hurdles to social media adoption by instructors in higher 
education.  The more faculty members understand the effective uses of social media for teaching and learning, and the better we get at learning how to 
balance “privacy” within the social sphere, the faster academic use of social media will proliferate across higher education and support student engagement 
and success.  We are continuously looking to work with faculty to gain more insight into how a social-academic media platform can be tailored 
to help the learning process across various disciplines in higher education.</p>-->
